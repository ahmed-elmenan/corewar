#include "ft_printf.h"

int	ft_intlen2(long long n)
{
	size_t count;

	count = 0;
	if (n < 0)
		count++;
	while (n)
	{
		if (n / 10)
		{
			count++;
			n /= 10;
		}
		else if (n / 10 == 0)
			break ;
	}
	return (count + 1);
}
